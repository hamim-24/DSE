export const getQuizzesByCategory = (category: string): Quiz[] => {
  return quizzes.filter((quiz) => quiz.category === category);
};

export const getFeaturedQuizzes = (): Quiz[] => {
  return quizzes.filter((quiz) => quiz.isFeatured);
};

export const getNewQuizzes = (): Quiz[] => {
  return quizzes.filter((quiz) => quiz.isNew);
};

export const getQuizById = (id: string): Quiz | undefined => {
  return quizzes.find((quiz) => quiz.id === id);
};

export const getQuizzes = (isSurvey = false): Quiz[] => {
  return quizzes.filter((quiz) => quiz.isSurvey === isSurvey);
};

export const getSurveys = (): Quiz[] => {
  return getQuizzes(true);
};
